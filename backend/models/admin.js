import { DataTypes } from 'sequelize';
import { sequelize } from '../config/sequelize.js';

const Admin = sequelize.define(
  'Admin',
  {
    ID: {
      type: DataTypes.BIGINT.UNSIGNED,
      primaryKey: true,
      autoIncrement: true,
      allowNull: false,
    },
    adminId: {
      type: DataTypes.STRING(50),
      allowNull: false, // it's auto-generated by MySQL
      unique: true,
    },
    userName: {
      type: DataTypes.STRING(255),
      allowNull: false,
    },
    emailAddress: {
      type: DataTypes.STRING(255),
      allowNull: true,
      unique: true,
      validate: {
        isEmail: true,
      },
    },
    phoneNumber: {
      type: DataTypes.STRING(15),
      allowNull: true,
      unique: true,
    },
    password: {
      type: DataTypes.TEXT,
      allowNull: false,
    },
    userType: {
      type: DataTypes.ENUM('Super Admin', 'Admin', 'Staff'),
      allowNull: false,
    },
    createAt: {
      type: DataTypes.DATE,
      defaultValue: DataTypes.NOW,
    },
    updateAt: {
      type: DataTypes.DATE,
      defaultValue: DataTypes.NOW,
    },
    verifiedUser: {
      type: DataTypes.BOOLEAN,
      allowNull: false,
      defaultValue: false,
    },
    adminHead: {
      type: DataTypes.BOOLEAN,
      allowNull: false,
      defaultValue: false,
    },
    verificationCode: {
      type: DataTypes.STRING(10),
      allowNull: true,
    },
    codeExpiresAt: {
      type: DataTypes.DATE,
      allowNull: true,
    },
    loginToken: {
      type: DataTypes.STRING(255),
      allowNull: true,
    },
    forceLogout: {
      type: DataTypes.BOOLEAN,
      defaultValue: false,
    }
  },
  {
    tableName: 'admin',
    timestamps: false,
  }
);

export default Admin;